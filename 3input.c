#include <stdio.h>

int main()
{
    int a, b;
    printf("Enter the value of a\n");
    // Taking the value of a from user
    scanf("%d", &a);
    printf("Enter the value of b\n");
    // Taking the value of b from the user 
    scanf("%d", &b);
    // Adding both this value 
    printf("Sum of a and b is %d", a + b);
    return 0;
}
